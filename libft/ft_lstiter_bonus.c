#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*ptr;

	ptr = lst;
	if (!ptr)
		return ;
	while (ptr)
	{
		f(ptr->content);
		ptr = ptr->next;
	}
}
